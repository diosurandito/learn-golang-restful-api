func TestListCategoriesSuccess(t *testing.T) {
	db := setupTestDB()
	truncateCategory(db)

	tx, _ := db.Begin()
	categoryRepository := repository.NewCategoryRepository()
	category1 := categoryRepository.Save(context.Background(), tx, domain.Category{
		Name: "Food",
	})
	category2 := categoryRepository.Save(context.Background(), tx, domain.Category{
		Name: "Drink",
	})
	tx.Commit()

	router := setupRouter(db)

	request := httptest.NewRequest(http.MethodGet, "http://localhost:3000/api/categories", nil)
	request.Header.Add("X-API-Key", "RAHASIA")

	recorder := httptest.NewRecorder()

	router.ServeHTTP(recorder, request)

	response := recorder.Result()
	assert.Equal(t, 200, response.StatusCode)

	body, _ := io.ReadAll(response.Body)
	var responseBody map[string]interface{}
	json.Unmarshal(body, &responseBody)

	responseCode := int(responseBody["code"].(float64))
	responseStatus := responseBody["status"]

	assert.Equal(t, 200, responseCode, "Both should be equal!")
	assert.Equal(t, "OK", responseStatus, "Both should be equal!")

	var categories = responseBody["data"].([]interface{})
	categoryResponse1 := categories[0].(map[string]interface{})
	categoryResponse2 := categories[1].(map[string]interface{})

	assert.Equal(t, category1.Id, int(categoryResponse1["id"].(float64)), "Both should be equal!")
	assert.Equal(t, category1.Name, categoryResponse1["name"], "Both should be equal!")

	assert.Equal(t, category2.Id, int(categoryResponse2["id"].(float64)), "Both should be equal!")
	assert.Equal(t, category2.Name, categoryResponse2["name"], "Both should be equal!")
}

